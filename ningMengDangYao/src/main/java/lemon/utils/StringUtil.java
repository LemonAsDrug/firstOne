package lemon.utils;

public class StringUtil {

    public static void main(String[] args) {
        String str = "WuCunYang";

        /**
         * 将字符串专为全小写
         */
        String str1 = str.toLowerCase();

        /**
         * 将字符串专为全大写
         */
        String str2 = str.toUpperCase();

        System.out.println(str1);
        System.out.println(str2);
    }
}
